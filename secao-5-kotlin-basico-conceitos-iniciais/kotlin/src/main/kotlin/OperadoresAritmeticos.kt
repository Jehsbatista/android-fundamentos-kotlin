fun main() {
    /*

    -----operadores aritméticos------
                + - * / %

    */

    var idade = 10
    idade += 1    //soma
    idade -= 3    //subtração
    idade *= 2    //multiplicaçào
    idade /= 2    //divisão
    println(idade) //resultado

    var idadee = 10 % 3  //utilizando % pegamos o resto
    println(idadee)


    //Incrementando valores//

    var incremento = 20
    incremento++              //utilizado ++ depois da variável, acessa o valor primeiro e depois incrementa em 1, passa a valer 21.
    println(incremento)

    var incrementoo = 21
    ++incrementoo             //utilizando ++ antes da variável, incrementa em 1 e depois acessa o valor, passa a valer 22.
    println(incrementoo)
}