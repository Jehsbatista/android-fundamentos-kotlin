import kotlin.math.*
//importando funcionalidades da biblioteca math
fun main() {
    println(max(10, 20))         //pega o valor máximo
    println(min(15, 20))         //pega o valor mínimo
    println(PI)                        // valor de PI
    println(E)                         //valor de E
    println(sqrt(9.74625))          //raiz quadrada de um float ou double
    println(round(7.74294724749))   //arredonda números quebrados
}